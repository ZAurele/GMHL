<header class="major"><h3 id="profilheader">Notifications</h3></header>

<dl>
    <dt><h4 id="notif">Notifications</h4></dt>
    
    <dd>
        <blockquote>
        Les notifications, indiquant de nouveaux messages / commentaires / messages privés sont respectivement (de gauche à droite) situés en haut à droite du site.</blockquote>

        <div align="center"><img src="images/aide/aide_1.png"/> <img src="images/aide/aide_2.png"/><br></div>
        
        <blockquote>Un clic sur l'un de ces symboles fait respectivement apparaitre les nouveaux messages / les messages modifiés, les nouveaux commentaires et les nouveaux messages privés / messages privés modifiés / commentaires de messages privés.
        </blockquote>
        
    </dd>
        
    <dt><h4 id="nonLu">Message lu / non lu</h4></dt>
        
        <blockquote>
        Pour indiquer qu'un message ou un message privé a été vu, il faut cliquer sur le bouton en forme de carré en bas des messages associés. Il est possible de reconsidérer qu'un message est non lu en cliquant sur la même case, cochée cette fois. Le fonctionnement est strictement identique pour les messages privés.
        </blockquote>
        
        <div align="center"><img src="images/aide/aide_3.png"/> <br></div>
        
        <blockquote>
        Pour les commentaires, un clic sur 'voir les commentaires' implique que l'ensemble des commentaires alors affichés sont considérés comme vus. 
        </blockquote>
        
        <div align="center"><img src="images/aide/aide_4.png"/> <br></div>
    </dd>
</dl>