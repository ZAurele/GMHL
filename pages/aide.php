<header class="major"><h3 id="messages">Les messages</h3></header>

<dl>
    <dt><h4 id="carac">Caractéristiques</h4></dt>
    <dd>
        <blockquote>
        Les messages sont caractérisés par un type, une catégorie et une priorité.
        </blockquote>
    </dd>
    
    <dt><h4 id="types">Types des messages</h4></dt>
    
    <dd>
        <blockquote>
        Les messages possèdent des types bien définis, ces types permettent de changer les fonctionnalités des messages.
        </blockquote>
    
        <ul>
            <li><b>"Nouvelles"</b>: c'est le type de message classique.</li>
            <div align="center"><img src="images/aide/14.png"/><br></div>
            <li><b>"Travaux"</b>: ce type de message permet à son créateur de mettre à jour le statut des travaux (Vue,Signalé,En cours,Fini).</li>
            <div align="center"><img src="images/aide/2.png"/>  <img src="images/aide/25.png"/><br></div>
            <li><b>"Evénement"</b>: les messages de ce type ont une date et sont rajoutés dans le calendrier.</li>
            <div align="center"><img src="images/aide/8.png"/> <img src="images/aide/14.png"/><br></div>
            <li><b>"Proposition"</b>: les utilisateurs peuvent voter (oui/peut-être/non) à ce genre de message.</li>
            <div align="center"><img src="images/aide/1.png"/> <img src="images/aide/26.png"/><br></div>
        </ul>
    </dd>
    
    <dt><h4 id="categ">Catégories des messages</h4></dt>
    
    <dd>
        <blockquote>
        Une catégorie est attribuée à chaque message, ces catégories sont définies par les utilisateurs, elles n'ont pour but que de classer les messages.<br>
        <div align="center"><img src="images/aide/15.png"/></div>
        </blockquote>
    </dd>
    
    <dt><h4 id="newcat">Nouvelle catégorie</h4></dt>
    
    <dd>
    <blockquote>
        N'importe qui peut rajouter une catégorie si elle ou une similaire ne sont pas déja présentes dans la liste.</br>
        <div align="center"><img src="images/aide/16.png"/><br></div>
        De fait merci de ne pas rajouter trop de catégories.</br>
        <div align="center"><img src="images/aide/17.png"/><br></div>
        Par défaut les nouvelles catégories n'ont pas d'icones, je dois les rajouter moi-même.
        </blockquote>
    
    </dd>
    
    <dt><h4 id="fav">Favoris</h4></dt>
    
    <dd>
        <blockquote>
        Il est possible de rajouter un message dans vos favoris en cliquant sur la petite étoile en bas des messages.</br></br>
        <div align="center"><img src="images/aide/18.png"/><br></div>
        Par la suite il est alors possible de classer les messages favoris via le lien du menu.
        </blockquote>
    </dd>
    
    <dt><h4 id="imp">Importance</h4></dt>
    
    <dd>
        <blockquote>
        Les messages peuvent être catégorisés en fonction de leur importance, cela n'a pour le moment aucune influence, à l'exeption d'une couleur de titre différent.<br>
        <div align="center"><img src="images/aide/7.png"/> <img src="images/aide/19.png"/></div>
        </blockquote>
    </dd>
    
    <dt><h4 id="date">Date</h4></dt>
    
    <dd>
        <blockquote>
        Que le message soit de type "événement" ou non il est possible de lui associer une date.</br>
        <div align="center"><img src="images/aide/20.png"/><br></div>
        Cette date est affichée au niveau du message. <br>
        <div align="center"><img src="images/aide/14.png"/><br></div>
        Pour les messages de type "événement" la date permet l'intégration du message dans le calendrier.
        </blockquote>
    </dd>
    
    <dt><h4 id="file">Fichier /image</h4></dt>
    
    <dd>
        <blockquote>
        Il est possible de joindre une image ou un fichier au message.</br>
        <div align="center"><img src="images/aide/21.png"/><br></div>
        Le message possède alors un lien vers cette image ou ce fichier.</br>
        <div align="center"><img src="images/aide/3.png"/><br></div>
        Une taille maximale des fichiers/images à ne pas dépasser est affichée.
        </blockquote>
    </dd>
    
    <dt><h4 id="file">Message privé</h4></dt>
    
    <dd>
        <blockquote>
        En cliquant sur le nom / numéro d'appartement d'une personne vous pouvez lui envoyer un message privé.<br>
        <div align="center"><img src="images/aide/9.png"/><br></div>
        Pour visualiser les messages privés, il faut cliquer sur l'icone message avec un chiffre en haut du site.<br>
        <div align="center"><img src="images/aide/10.png"/> <img src="images/aide/11.png"/></div>
        </blockquote>
    </dd>
    
    <dt><h4 id="file">Commentaires</h4></dt>
    
    <dd>
        <blockquote>
        <br>
        Il est possible de commenter et visualiser les messages en cliquant sur les liens en dessous des messages.<br>
        <div align="center"><img src="images/aide/4.png"/></div>
        </blockquote>
    </dd>
</dl>
    
