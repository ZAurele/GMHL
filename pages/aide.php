<header class="major"><h3 id="site">Le site</h3></header>

<dl>
    <dt><h4 id="principe">Principe</h4></dt>
    
    <dd>
    <blockquote>
    Le site est basé sur une gestion de messages, de commentaires et de notifications.<br><br>

    Le but est de faciliter le partage d'informations dans l'immeuble entre propriétaires/locataires/syndicats/voisins.
    </blockquote>
    </dd>
    
    <dt><h4 id="acces">Accès au site</h4></dt>
    
    <dd>
    <blockquote>
    Les codes d'accès au site sont au nombre de 2 par logement, mais il est possible de me demander des accès supplémentaires, notamment si vous êtes locataire et que vous voulez un accès pour le propriétaire de votre logement.
    </blockquote>
    </dd>
    
    <dt><h4 id="menu">Menu</h4></dt>
    
    <dd>
        <blockquote>
        Le menu du site est situé en haut à gauche, il permet d'accéder aux pages, de filtrer les messages et d'accéder au calendrier.
        <div align="center"><img src="images/aide/45.png"/></div>
        </blockquote>
    </dd>

    <dt><h4 id="charte">Charte</h4></dt>
    
    <dd>
        <blockquote>
        Seul les résidents de l'immeuble ont accès au site, il ne me semble donc pas utile de définir une charte pour le site. </br></br>
        
        Néanmoins, il est possible de définir ensemble une charte dans la catégorie 'Siteweb' si besoin.<br><br>
        
        Si jamais des personnes font un usage abusif du site j'effacerais tout simplement l'accès.
        </blockquote>
    </dd>    
    
    <dt><h4 id="bugs">Bugs / Modifications</h4></dt>
    
    <dd>
        <blockquote>
        Ce site n'est pas un site commercial, je le développe bénévolement. </br></br>
        Si jamais il y a des bugs ou des modifications à apporter vous pouvez me le signaler mais je ne promets rien quant à la réalisation et les délais.
        </blockquote>
    </dd>
    
</dl>

