<p style="text-align: justify;">Le PNR de Millevaches en Limousin a élaboré un questionnaire pour toute personne exerçant en France et désirant connaître les aides qu'elle peut mobiliser.<u><b>compris ou non dans le PNR </b></u>! Si vous souhaitez évaluer vos droits au financement de mesures de protection des troupeaux. 
Ce questionnaire vous permet d’entrer les critères d’éligibilité aux subventions (taille et mode de conduite du troupeau, classement de la commune de pâturage en cercles), d’obtenir la 
liste des mesures auxquelles vous avez droit, leur pourcentage de prise en charge par l’Etat et l’Union européenne et le plafond d’aide pour les mesures. 
<br></br>
 Vous obtiendrez en outre des informations sur le montage des dossiers ainsi que les liens utiles vers les documents de cadrage de ce financement. Pour rappel, <b>les dossiers doivent être envoyés en DDT avant le 31 mai.</b> 
 Les troupeaux bovins ou équins ne sont pas éligibles à cette subvention.</p>
