<h3><p style="text-align: center;">Parc Naturel Régional de Millevaches </p></h3>
<p style="text-align: justify;"> Le parc de Millevaches est un établissement public chargé de préserver les patrimoines naturels, culturels, paysagers, architecturaux et socio-économiques de ses 124 communes adhérentes. Il s’est engagé en 2021, sous l’impulsion de la région Nouvelle-Aquitaine, sur la question des grands prédateurs, avec pour mission de construire un groupe d’entente pour favoriser la collaboration des acteurs, créer les conditions d’une cohabitation au sein du vivant et faire émerger des solutions collectives efficaces et adaptées, approfondir le suivi du loup pour mieux anticiper les risques de prédation et accompagner les éleveurs pour les aider à protéger et défendre leurs troupeaux. Le parc est alors un relais entre les services de l’Etat et tous les acteurs du territoire. 
</p>
